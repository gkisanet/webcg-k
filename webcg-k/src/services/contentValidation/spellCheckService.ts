/**
 * 로컬 사전 기반 맞춤법 검사
 *
 * ■ 알고리즘:
 *   1. 사전의 각 [틀린 표현]을 텍스트에서 검색
 *   2. "확인 (올바름)" 규칙은 건너뜀 (긍정 확인용으로만 존재)
 *   3. 매칭 발견 시 SpellingIssue 반환
 *
 * @param text - 검사할 순수 텍스트 (HTML 제거 후)
 * @returns 발견된 맞춤법 이슈 배열
 */
export function checkSpelling(text: string): SpellingIssue[] {
	const issues: SpellingIssue[] = [];

	for (const [wrong, correct, rule] of SPELL_DICTIONARY) {
		// "확인" 규칙은 레퍼런스용이므로 건너뜀
		if (rule.includes("확인")) continue;
		// 틀린 표현 == 올바른 표현이면 건너뜀
		if (wrong === correct) continue;

		if (text.includes(wrong)) {
			issues.push({
				original: wrong,
				corrected: correct,
				rule,
			});
		}
	}

	return issues;
}
