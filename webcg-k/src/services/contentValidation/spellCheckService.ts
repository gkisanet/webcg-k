/**
 * 맞춤법 검사 서비스 — 로컬 사전 기반 한국어 맞춤법 교정
 *
 * ■ Why 로컬 사전 우선?
 *   방송 현장에서는 네트워크 지연이 치명적.
 *   자주 틀리는 200쌍의 로컬 사전으로 즉시 응답(0ms).
 *   Gemini AI 심층 검사는 "맞춤법 검사 버튼" 클릭 시에만 옵션 호출.
 *
 * ■ 검사 카테고리:
 *   - spelling: 맞춤법 오류 (됬다→됐다)
 *   - spacing: 띄어쓰기 (안돼→안 돼)
 *   - grammar: 문법 (되다/돼다 혼동)
 */

// ─── 타입 ─────────────────────────────────────────────────────────

export interface SpellingIssue {
	original: string;
	corrected: string;
	rule: string;
}

// ─── 로컬 사전 ────────────────────────────────────────────────────

